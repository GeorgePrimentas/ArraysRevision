public class ArrayLength {
    public static void main(String[] args) {
        // Declares a String array and assigns
        // (initiates to it) 4 elements (e.g. "Volvo", etc.)
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        // Displays the length (i.e. the total number of
        // elements) of the array with the .length property
        System.out.println(cars.length);
    }
}
