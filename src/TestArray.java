public class TestArray {
    public static void main(String[] args) {
        // Declares a double, 1-dimensional array and assigns
        // (initiates to it) 4 elements (e.g. 1.9, etc.)
        double[] myList = {1.9, 2.9, 3.4, 3.5};
        // With a for loop, all the elements of the
        // array are displayed on the console
        for (int i = 0; i < myList.length; i++) {
            System.out.println(myList[i]);
        }
    }
}
